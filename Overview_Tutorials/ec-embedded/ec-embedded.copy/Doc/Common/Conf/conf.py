from sphinx import addnodes

def rebuild_doctree(app, env, added, changed, removed):
    """
    Determine which doctrees need to be rebuilt based on the presence of ".. only::" directives.

    This function is used with the Sphinx "env-get-outdated" event.
    It inspects cached doctrees (without reparsing .rst files) and marks any document
    containing a Sphinx 'only' node for rebuilding.
    """
    outdated = set(added) | set(changed) | set(removed)

    rebuild = set()
    for docname in env.found_docs:
        if docname in outdated:
            continue
        try:
            doctree = env.get_doctree(docname)
            if any(isinstance(node, addnodes.only) for node in doctree.traverse()):
                rebuild.add(docname)
        except Exception as e:
            rebuild.add(docname)
            continue

    return list(rebuild)

